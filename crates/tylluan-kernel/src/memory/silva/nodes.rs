    /// Manual weight decay using half-life exponential formula.
    /// T_half = 14 days = 1_209_600 seconds.
    /// new_weight = old_weight * exp(-lambda * elapsed_secs)
    /// where lambda = ln(2) / T_half
    pub async fn decay_node(&self, id: &str, elapsed_secs: i64) -> Result<()> {
        tokio::task::block_in_place(|| {
            let conn = self.conn.blocking_lock();
            let lambda = std::f64::consts::LN_2 / 1_209_600.0_f64;
            let decay_factor = (-lambda * elapsed_secs as f64).exp();
            conn.execute(
                "UPDATE nodes SET
                    weight = MAX(weight * ?2, 0.01),
                    updated_at = CURRENT_TIMESTAMP
                 WHERE id = ?1",
                params![id, decay_factor],
            )?;
            Ok::<(), anyhow::Error>(())
        })?;
        Ok(())
    }

    /// Detect communities (Louvain) and persist to node_communities.
    pub async fn detect_communities(&self) -> Result<HashMap<String, usize>> {
        let nodes = self.get_nodes_limited(1000, 0.0).await?;
        let edges = self.get_all_edges().await?;
        let node_ids: Vec<String> = nodes.iter().map(|n| n.id.clone()).collect();
        let mut graph = WeightedGraph::new(node_ids);
        for edge in edges {
            let source = edge.get("source").and_then(|v| v.as_str()).unwrap_or_default().to_string();
            let target = edge.get("target").and_then(|v| v.as_str()).unwrap_or_default().to_string();
            let weight = edge.get("weight").and_then(|v| v.as_f64()).unwrap_or(1.0);
            if !source.is_empty() && !target.is_empty() {
                graph.add_edge(&source, &target, weight);
            }
        }
        let communities = graph.find_communities();
        tokio::task::block_in_place(|| {
            let conn = self.conn.blocking_lock();
            let mut stmt = conn.prepare(
                "INSERT INTO node_communities (node_id, cluster_id, updated_at)
                 VALUES (?1, ?2, CURRENT_TIMESTAMP)
                 ON CONFLICT(node_id) DO UPDATE SET cluster_id = excluded.cluster_id, updated_at = CURRENT_TIMESTAMP"
            )?;
            for (node_id, cluster_id) in &communities {
                stmt.execute(params![node_id, *cluster_id as i64])?;
            }
            let mut stmt_node = conn.prepare("UPDATE nodes SET cluster_id = ?2 WHERE id = ?1")?;
            for (node_id, cluster_id) in &communities {
                stmt_node.execute(params![node_id, *cluster_id as i64])?;
            }
            Ok::<(), anyhow::Error>(())
        })?;
        Ok(communities)
    }

