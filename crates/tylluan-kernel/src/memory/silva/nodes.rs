    /// Apply lazy Ebbinghaus decay to a single node based on elapsed time.
    pub async fn apply_node_decay(&self, node_id: &str) -> Result<f64> {
        tokio::task::block_in_place(|| {
            let conn = self.conn.blocking_lock();
            let result: std::result::Result<(f64, Option<i64>), rusqlite::Error> = conn.query_row(
                "SELECT weight, last_touched FROM nodes WHERE id = ?1",
                params![node_id],
                |row| Ok((row.get(0)?, row.get(1)?)),
            );
            let (weight, last_touched) = match result {
                Ok((w, Some(lt))) => (w, lt),
                Ok((w, None)) => return Ok(w),
                Err(_) => return Err(anyhow::anyhow!("Node not found: {}", node_id)),
            };
            let now = std::time::SystemTime::now()
                .duration_since(std::time::UNIX_EPOCH)
                .unwrap_or_default()
                .as_secs() as i64;
            let elapsed_secs = (now - last_touched) as f64;
            let hours = elapsed_secs / 3600.0;
            if hours < 1.0 { return Ok(weight); }

            let trace_count: i64 = conn.query_row(
                "SELECT COUNT(*) FROM node_traces WHERE node_id = ?1 AND touched_at >= ?2",
                params![node_id, now - 86400], |r| r.get(0),
            ).unwrap_or(0);
            let heat = (trace_count as f64 / 240.0).min(1.0);
            let lambda = 0.05;
            let in_degree: i64 = conn.query_row(
                "SELECT COUNT(*) FROM edges WHERE target = ?1",
                params![node_id], |r| r.get(0),
            ).unwrap_or(0);
            let protection = 1.0 / (1.0 + (in_degree as f64).ln().max(0.0));
            let mut lambda_eff = lambda * protection * (1.0 - 0.3 * heat);
            let rejected_count: i64 = conn.query_row(
                "SELECT COUNT(*) FROM node_traces WHERE node_id = ?1 AND trace_type = 'rejected' AND touched_at >= ?2",
                params![node_id, now - 86400], |r| r.get(0),
            ).unwrap_or(0);
            if rejected_count > 5 { lambda_eff *= 3.0; }
            // Adaptive decay by node type prefix
            let type_multiplier: f64 = if node_id.starts_with("lesson:intent:") { 3.0 }
                else if node_id.starts_with("concept:") { 0.33 }
                else { 1.0 };
            lambda_eff *= type_multiplier;
            let new_weight = weight * (-lambda_eff * hours).exp();
            let salience = new_weight * (1.0 + (trace_count as f64 + 1.0).ln() * 0.1);
            conn.execute(
                "UPDATE nodes SET weight = ?1, salience_score = ?2 WHERE id = ?3",
                params![new_weight, salience, node_id],
            )?;
            Ok(new_weight)
        })
    }

